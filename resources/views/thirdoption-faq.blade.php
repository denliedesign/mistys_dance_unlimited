               <p>
                   <strong>Welcome to Third Option™ Learning Hub</strong>
                   <br>This FAQ Sheet has been prepared to answer parent questions about
                   the Third Option™ Learning Hub and to help parents decide whether
                   or not it would be a good fit to support their child’s education for fall.
                   <br><br>
                   <strong>What is the purpose of the Third Option™ Learning Hub?</strong>
                   <br>Third Option™ Learning Hub is a way our business (Misty’s Dance
                   Unlimited, LLC) can support local schools by keeping enrollments
                   in local districts via Coulee Region Virtual Academy (CRVA). It’s also
                   a way to offer support for parents who may not be comfortable
                   going back to traditional school this fall and who also may be
                   looking for more support and socialization than may be available
                   at home.
                   <br><br>
                   <strong>Is this only for children in the CRVA?</strong>
                   <br>We will be open all of the first semester to serve CRVA students.  But we also have space to serve families who need support for the 30-day virtual start to school.
                   <br><br>
                   <strong>How is Third Option™ Learning Hub different from a school?</strong>
                   <br>Third Option™ Learning Hub is a service offering at Misty’s Dance Unlimited, LLC, an independent youth learning facility, which does not operate under the same guidelines as public schools. As a private organization, we are able to set our own class sizes, educational best practices and standards of care. Misty’s Dance Unlimited, LLC follows our national association guidelines and is independently youth protection certified.
                   <br><br>
                   <strong>Where will the Third Option™ Learning Hub be held?</strong>
                   <br>All classes will meet at the Misty’s Dance Unlimited facility which is located at 923 12th Ave. South in Onalaska. The facility has 8 instructional rooms, a lunch area, parking, security systems, state of the art air handling capacity, and outdoor space.
                   <br><br>
                   <strong>What ages can the Third Option™ Learning Hub
                       currently serve?</strong>
                   <br>Third Option™ Learning Hub is currently able to serve
                   7-18 year old students. We are working with the Department
                   of Children and Families to explore licensing options that would
                   allow us to serve children ages 4-6 in the future, but they are
                   NOT giving out any emergency licenses to serve those ages
                   at this time.
                   <br><br>
                   <strong>What is the daily schedule?</strong>
                   <br>We have two options: Half days (8:30am-11:30am) or
                   full days (8:30am-2:30pm). According to our current
                   understanding of the CRVA schedule, mornings will be
                   focused on virtual learning (at appropriate lengths for different ages)
                   and afternoons will be for extended study and/or enrichment activities.
                   There will also be time each day for lunch, physical activity and getting outside.
                   <br><br>
                   <strong>What do the full day students do for lunch?</strong>
                   <br>Students will bring their own lunch and snacks each day.
                   <br><br>
                   <strong>What are class sizes?</strong>
                   <br>Each class will be limited to 10 students, which will be considered
                   a cohort. Each cohort will stay together for learning, lunch and
                   enrichment activities to minimize community exposure.
                   <br><br>
                   Our initial offering will be 7 different cohorts of 10 students each
                   according to the following grades: 2nd, 3rd, 4th, 5th, 6th, 7th,
                   8th-12th. Cohorts may be combined if needed to make groups of 10.
                   For example, if there are five 6th graders and five 7th graders,
                   they would be combined into one cohort of 10.
                   <br><br>
                   <strong>What if I try to register and it’s full?</strong>
                   <br>We will keep a waiting list and reach out if a space opens up. We are also actively working with other community organizations to seek additional space and/or to provide other leaders with a framework to provide this type of service on their own.
                   <br><br>
                   <strong>Do you have surround care available?</strong>
                   <br>We will add this feature if there is enough need. Please indicate your interest <a href="https://docs.google.com/forms/d/e/1FAIpQLSfCUEby7MwH8CMLXTfZy1nTWZL6OtwaIw3SAnq6Fr9-ITgs9g/viewform" target="_blank">here</a>.
                   <br><br>
                   <strong>Who will be supervising the students?</strong>
                   <br>Each classroom will be staffed by background checked, adult Instructional Guides whose role is to help the students to log-in to CRVA and navigate virtual learning each day. Our instructional guides are also there to create a sense of community, foster safe socialization and facilitate daily enrichment activities among learners. In some cases, there will be one instructional guide for the morning and one for the afternoon to minimize teacher exposure and to maximize classroom coverage for when subs are needed. The School District of La Crosse has offered to include our Instructional Guides in their teacher training for the CRVA.
                   <br><br>
                   <strong>Is there a fee?</strong>
                   <br>Yes, Third Option™ Learning Hub is a fee-based service similar to a day camp or other youth development activities. Registration requires a one-time, non-refundable registration fee of $45 which will hold your space between now and the start of classes.
                   Beginning the first week of classes, weekly fees of $25/per day for half days or $39/per day for full days will be charged to your account on Monday of each week. For your convenience weekly fees will be drafted automatically beginning Monday, Aug. 31 until you terminate your enrollment or the end of the CRVA. Need based, financial assistance is available.
                   <br><br>
                   Please note: A one week notice is required to withdraw.
                   <br><br>
                   <strong>Sibling Discounts</strong>
                   <br>Yes! We do have a sibling discount for this program of 30%. So if you have one child in 4th grade and one in 2nd and you are choosing part-time programming, you would pay $25/day for the first child and $17.50/day for the 2nd child.
                   <br><br>
                   <strong>Why is there a fee?</strong>
                   <br>Weekly fees are charged to cover expenses such as the hiring of staff, background checks, training, administration, equipment, wifi, materials, cleaning and space.
                   <br><br>
                   <strong>Is there financial assistance?</strong>
                       <br>Yes! Community support is part of our core philosophy. Need based, financial assistance will
                   be available. To apply for financial assistance <a href="https://docs.google.com/forms/d/e/1FAIpQLSfCUEby7MwH8CMLXTfZy1nTWZL6OtwaIw3SAnq6Fr9-ITgs9g/viewform" target="_blank">click here</a>.
                   <br><br>
                   <strong>Would my child with special educational needs be welcome?</strong>
                       <br>The La Crosse Area Autism Foundation has graciously agreed to
                   provide support for students with special educational needs.
                   To apply for special educational support <a href="https://docs.google.com/forms/d/e/1FAIpQLSfCUEby7MwH8CMLXTfZy1nTWZL6OtwaIw3SAnq6Fr9-ITgs9g/viewform" target="_blank">click here</a>.
                   <br><br>
                   <strong>What protections and procedures are in place to fight community
                       spread of COVID-19?</strong>
                       <br>Misty’s Dance Unlimited, LLC follows the More Than Just Great
                   Dancing!® Safer Studio™ policies and procedures which focuses on
                   a hierarchy of measures beginning with avoiding contact with
                   anyone with symptoms, followed by frequent hand cleaning and
                   good hygiene practices, amplified cleaning, and minimizing
                   contact and mingling.
                   <br><br>
                   <strong>What measures are put in place to foster a sense of normalcy for
                       children in a pandemic world?</strong>
                       <br>The mere act of getting up in the morning, getting dressed and “going to school” can be a significant anchor for a child in a turbulent time. Daily routines are stabilizing for children. Our small class sizes and structure minimize risk and maximize the joyful parts of schools such as socialization. Our classrooms are well lit and cheery. The artwork of children and youth is displayed prominently down our hallways. Important routines of school such as classroom greetings, newsletters, enrichment activities and community building are not only still present in our program, they are more important than ever.
                   <br><br>
                   <strong>Do you do temperature checking?</strong>
                       <br>Our staff is temperature checked on a daily basis, but we do not temperature check students.
                   <br><br>
                   <strong>Will the staff and students have to wear masks?</strong>
                       <br>Our staff have been wearing masks all summer and will continue to do so while the County is in red and orange. The current State Emergency Order #1 allows for instructors to step back more than six feet from students and lower their mask to give large group instructions.
                   <br><br>
                   State Emergency Order #1 requires all students over the age of five to wear a cloth face covering while indoors until September 28, 2020. Students do not have to wear a face covering while outdoors if spread out appropriately from other children. Once the emergency order expires, parents will be allowed to make their own decision for their children regarding masks and face
                   coverings unless another order is put into place.
                   <br><br>
                   <strong>What is your illness protocol?</strong>
                   <br>Staff, parents and students should know the symptoms of COVID-19:
                    <ul>
                       <li> Fever</li>
                       <li> Cough</li>
                       <li> Shortness of breath</li>
                       <li> Diarrhea (watery)</li>
                       <li> Sore throat/congestion</li>
                       <li> Headache</li>
                       <li> Chills</li>
                       <li> Muscle and joint pain (multiple)</li>
                       <li> Nausea or vomiting</li>
                       <li> Loss of sense of smell</li>
                   </ul>
                   Staff and students should notify the Third Option Learning™ Hub
                   administrators and contact healthcare providers if they develop symptoms
                   of COVID-19 or any other illness and will be asked to STAY HOME until they are symptom-free
                   for 72 hours. Any student who becomes unwell during the school day will wait in a special isolation area, which is supervised by a staff member, until a parent or guardian can arrive.
                   <br><br>
                   Students or staff who test positively for COVID-19 will follow the country’s current recommendations for self isolation. Students may take up to a two week leave of absence and keep their place in the program. If there is a positive report of COVID-19 in our learning community, we will follow the county’s current guidelines for notifications.
                   <br><br>
               <strong>How do I register?</strong>
               <ul>
                   <li> Registration begins Saturday, Aug. 1 at 8am. The first round of registration will be those who are seeking FULL DAY, 5 DAY placement. Remaining spaces will be made available on Monday at noon for those seeking half days or fewer than five days per week.</li>

                   <li> Registration requires a $45, non-refundable registration fee. Weekly fees will begin on Monday, August 31 and will be charged weekly on Mondays.</li>

